import React, { useEffect, useState } from "react";
import { useNavigate } from "react-router-dom";
import {
    FilePenLineIcon,
    PencilIcon,
    PlusIcon,
    TrashIcon,
    UploadCloudIcon,
    XIcon,
    UploadCloud,
} from "lucide-react";
import { dummyResumeData } from "../assets/assets";
import { useSelector } from "react-redux";
import api from "../configs/api";
import toast from "react-hot-toast";
import pdfToText from "react-pdftotext";

const Dashboard = () => {
    const { user, token } = useSelector((state) => state.auth);

    const colors = ["#9333ea", "#d97706", "#dc2626", "#0284c7", "#16a34a"];
    const [allResumes, setAllResumes] = React.useState([]);
    const [showCreateResume, setShowCreateResume] = useState(false);
    const [showUploadResume, setShowUploadResume] = useState(false);
    const [title, setTitle] = useState("");
    const [resume, setResume] = useState(null);
    const [editResumeId, setEditResumeId] = useState("");
    const [isLoading, setIsLoading] = useState(false);
    const navigate = useNavigate();

    const loadAllResumes = async () => {
        setAllResumes(dummyResumeData);
    };

    const createResume = async (e) => {
        try {
            e.preventDefault();
            const { data } = await api.post(
                "/api/resumes/create",
                { title },
                {
                    headers: {
                        Authorization: `Bearer ${token}`,
                    },
                },
            );

            setAllResumes([...allResumes, data.resume]);
            setTitle("");
            setShowCreateResume(false);
            navigate(`/app/builder/${data.resume._id}`);
        } catch (error) {
            toast.error(error?.response?.data?.message || error.message);
        }
    };

    const uploadResume = async (e) => {
        e.preventDefault();
        setIsLoading(true);

        try {
            if (!resume) {
                toast.error("Please select a resume file");
                return;
            }

            const resumeText = await pdfToText(resume);

            if (!resumeText || resumeText.trim().length < 30) {
                toast.error("Unable to extract text from resume");
                return;
            }
            const { data } = await api.post(
                "/api/ai/upload-resume",
                {
                    title,
                    resumeText,
                },
                {
                    headers: {
                        Authorization: `Bearer ${token}`,
                    },
                },
            );

            setTitle("");
            setResume(null);
            setShowUploadResume(false);

            navigate(`/app/builder/${data.resumeId}`);
        } catch (error) {
            toast.error(error?.response?.data?.message || error.message);
        } finally {
            setIsLoading(false);
        }
    };

    const editTitle = async (e) => {
        e.preventDefault();
        setEditResumeId("");
    };

    const deleteResume = async (resumeId) => {
        const confirm = window.confirm(
            "Are you sure you want to delete this resume?",
        );
        if (confirm) {
            setAllResumes((prev) =>
                prev.filter((resume) => resume._id !== resumeId),
            );
        }
    };

    useEffect(() => {
        loadAllResumes();
    }, []);

    return (
        <div>
            <div className="max-w-7xl mx-auto px-4 py-8">
                <p className="text-2x1 font-medium mb-6 bg-gradient-to-r from-slate-600 to-slate-700 bg-clip-text text-transparent sm:hiddenl">
                    Welcome, Kanish
                </p>

                <div className="flex gap-4">
                    <button
                        onClick={() => setShowCreateResume(true)}
                        className="w-full bg-white sm:max-w-36 h-48 flex flex-col items-center justify-center rounded-lg gap-2 text-slate-600 border border-dashed border-slate-300 group hover: border-purple-500 hover: shadow-lg transition-all duration-300 cursor-pointer"
                    >
                        <PlusIcon className="size-11 transition-all duration-300 p-2.5 bg-gradient-to-br from-indigo-300 to-indigo-500 text-white rounded-full" />
                        <p className="text-sm group-hover: text-indigo-600 transition-all duration-300">
                            Create Resume
                        </p>
                    </button>

                    <button
                        onClick={() => setShowUploadResume(true)}
                        className="w-full bg-white sm:max-w-36 h-48 flex flex-col items-center justify-center rounded-lg gap-2 text-slate-600 border border-dashed border-slate-300 group hover: border-indigo-500 hover: shadow-lg transition-all duration-300 cursor-pointer"
                    >
                        <UploadCloudIcon className="size-11 transition-all duration-300 p-2.5 bg-gradient-to-br from-purple-300 to-purple-500 text-white rounded-full" />
                        <p className="text-sm group-hover: text-purple-600 transition-all duration-300">
                            Upload Existing
                        </p>
                    </button>
                </div>

                <hr className="border-slate-300 my-6 sm:w-[305px]" />

                <div className="grid grid-cols-2 sm:flex flex-wrap gap-4">
                    {allResumes.map((resume, i) => {
                        const baseColor = colors[i % colors.length];
                        return (
                            <button
                                key={i}
                                onClick={() =>
                                    navigate(`/app/builder/${resume._id}`)
                                }
                                className="relative group w-full sm:max-w-36 h-48 flex flex-col items-center justify-center rounded-lg gap-2 border hover:shadow-lg transition-all duration-300 cursor-pointer"
                                style={{
                                    background: `linear-gradient(135deg, ${baseColor}10, ${baseColor}40)`,
                                    borderColor: `${baseColor}40`,
                                }}
                            >
                                <FilePenLineIcon
                                    className="size-7 transition-all group-hover:scale-105"
                                    style={{ color: baseColor }}
                                />

                                <p
                                    className="text-sm transition-all group-hover:scale-105 px-2 text-center"
                                    style={{ color: baseColor }}
                                >
                                    {resume.title}
                                </p>

                                <p
                                    className="absolute bottom-1 text-[11px] opacity-80 group-hover:opacity-100 transition-all duration-300 px-2 text-center"
                                    style={{ color: baseColor }}
                                >
                                    Updated on{" "}
                                    {new Date(
                                        resume.updatedAt,
                                    ).toLocaleDateString()}
                                </p>

                                <div
                                    onClick={(e) => e.stopPropagation}
                                    className="absolute top-1 right-1 hidden items-center group-hover:flex"
                                >
                                    <TrashIcon
                                        onClick={(e) => {
                                            e.stopPropagation();
                                            deleteResume(resume._id);
                                        }}
                                        className="size-7 p-1.5 hover:bg-white/50 rounded text-slate-700 transition-colors"
                                    />
                                    <PencilIcon
                                        onClick={(e) => {
                                            e.stopPropagation();
                                            setEditResumeId(resume._id);
                                            setTitle(resume.title);
                                        }}
                                        className="size-7 p-1.5 hover:bg-white/50 rounded text-slate-700 transition-colors"
                                    />
                                </div>
                            </button>
                        );
                    })}
                </div>

                <div>
                    {showCreateResume && (
                        <form
                            onSubmit={createResume}
                            onClick={() => setShowCreateResume(false)}
                            className="fixed inset-0 z-10 flex items-center justify-center bg-black/70 backdrop-blur"
                        >
                            <div
                                onClick={(e) => e.stopPropagation()}
                                className="relative bg-slate-50 border shadow-mdrounded-lg w-full max-w-md p-6"
                            >
                                <h2 className="text-xl font-semibold mb-4">
                                    Create a Resume
                                </h2>

                                <input
                                    type="text"
                                    placeholder="Enter resume title"
                                    className="w-full px-4 py-2 mb-4 border rounded focus:border-green-600 focus:ring-2 focus:ring-green-600 outline-none"
                                    required
                                    onChange={(e) => setTitle(e.target.value)}
                                    value={title}
                                />

                                <button
                                    type="submit"
                                    className="w-full py-2 bg-green-600 text-white rounded hover:bg-green-700 transition-colors"
                                >
                                    Create Resume
                                </button>

                                <XIcon
                                    className="absolute top-4 right-4 text-slate-400 hover:text-slate-600 cursor-pointer transition-colors"
                                    onClick={() => {
                                        setShowCreateResume(false);
                                        setTitle("");
                                    }}
                                />
                            </div>
                        </form>
                    )}

                    {showUploadResume && (
                        <form
                            onSubmit={uploadResume}
                            onClick={() => setShowUploadResume(false)}
                            className="fixed inset-0 z-10 flex items-center justify-center bg-black/70 backdrop-blur"
                        >
                            <div
                                onClick={(e) => e.stopPropagation()}
                                className="relative bg-slate-50 border shadow-mdrounded-lg w-full max-w-md p-6"
                            >
                                <h2 className="text-xl font-semibold mb-4">
                                    Upload Resume
                                </h2>

                                <input
                                    type="text"
                                    placeholder="Enter resume title"
                                    className="w-full px-4 py-2 mb-4 border rounded focus:border-green-600 focus:ring-2 focus:ring-green-600 outline-none"
                                    required
                                    onChange={(e) => setTitle(e.target.value)}
                                    value={title}
                                />

                                <div>
                                    <label
                                        htmlFor="resume-input"
                                        className="block text-sm text-slate-700"
                                    >
                                        Select resume file
                                        <div className="flex flex-col items-center justify-center gap-2 border group text-slate-400 border-slate-400 border-dashed rounded-md p-4 py-10 my-4 hover:border-green-500 hover:text-green-700 cursor-pointer transition-colors">
                                            {resume ? (
                                                <p className="text-green-700">
                                                    {resume.name}
                                                </p>
                                            ) : (
                                                <>
                                                    <UploadCloud className="size-14 stroke-1" />
                                                    <p>Upload resume</p>
                                                </>
                                            )}
                                        </div>
                                    </label>

                                    <input
                                        type="file"
                                        id="resume-input"
                                        className="hidden"
                                        onChange={(e) =>
                                            setResume(e.target.files[0])
                                        }
                                    />
                                </div>
                                <button
                                    type="submit"
                                    className="w-full py-2 bg-green-600 text-white rounded hover:bg-green-700 transition-colors"
                                >
                                    Upload Resume
                                </button>

                                <XIcon
                                    className="absolute top-4 right-4 text-slate-400 hover:text-slate-600 cursor-pointer transition-colors"
                                    onClick={() => {
                                        setShowUploadResume(false);
                                        setTitle("");
                                    }}
                                />
                            </div>
                        </form>
                    )}

                    {editResumeId && (
                        <form
                            onSubmit={editTitle}
                            onClick={() => setEditResumeId("")}
                            className="fixed inset-0 z-10 flex items-center justify-center bg-black/70 backdrop-blur"
                        >
                            <div
                                onClick={(e) => e.stopPropagation()}
                                className="relative bg-slate-50 border shadow-mdrounded-lg w-full max-w-md p-6"
                            >
                                <h2 className="text-xl font-semibold mb-4">
                                    Edit Resume Title
                                </h2>

                                <input
                                    type="text"
                                    placeholder="Enter resume title"
                                    className="w-full px-4 py-2 mb-4 border rounded focus:border-green-600 focus:ring-2 focus:ring-green-600 outline-none"
                                    required
                                    onChange={(e) => setTitle(e.target.value)}
                                    value={title}
                                />

                                <button
                                    type="submit"
                                    className="w-full py-2 bg-green-600 text-white rounded hover:bg-green-700 transition-colors"
                                >
                                    Update
                                </button>

                                <XIcon
                                    className="absolute top-4 right-4 text-slate-400 hover:text-slate-600 cursor-pointer transition-colors"
                                    onClick={() => {
                                        setEditResumeId("");
                                        setTitle("");
                                    }}
                                />
                            </div>
                        </form>
                    )}
                </div>
            </div>
        </div>
    );
};

export default Dashboard;
